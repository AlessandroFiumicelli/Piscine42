int		ft_atoi(char *str)
{
	int				nbr;
	int				neg;
	int				i;

	nbr = 0;
	neg = 0;
	i = 0;
	while (str[i] != '\0' && str[i] <= 32)
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr = nbr * 10 + (str[i] - 48);
		i++;
	}
	return (neg) ? -nbr : nbr;
}
